1. x = 2
This expression evalate/return the integer 2.

2. puts x = 2
This expression evaluates to nil after outputing string "2". The puts method always evaluates 
to nil. The puts method always returns a string. Puts is shorthand put-string

3. p name = "Joe"
This outputs "Joe" and also returns/evaluates to "Joe". This is because p outputs and inspects
the string.

4. four = "four"
This expression evaluates to "four"

