#What is exception handling and what problem does it solve?

#Exception handling is how we can design out code, during risky sections, to take 
#certain actions if some unusual things happen that could ruin the functionality of
#your code. It seems similiar to if/then conditional flow statements but instead use 
#the words "begin", "rescue", "end". The "rescue" keyword would be used to specify
#an action for our program to take should any form of error occur in early code.
#We can also use "rescue" keyword to specify actions to take with specific errors.
#e.g. We can have specific messages printed out when we have a 'can't combine an 
# integer to string" error by specifying condion after the rescue keyword like a 
# boolean value.
